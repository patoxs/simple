                            ¿QUÉ ES SIMPLE?
                        </h3>
                        <p class="item-text">
                            SIMPLE es un sistema modelador de procesos gráficos que permite definir
                            tareas y aplicar lógica de decisiones de negocio, permitiendo la
                            implementación directa de los procesos electrónicos definidos, entregando
                            una solución flexible, amigable y sencilla a instituciones públicas para
                            digitalizar sus trámites, tanto internos como externos. Es importante
                            aclarar que SIMPLE no es un gestor de documentos.
                        </p>
